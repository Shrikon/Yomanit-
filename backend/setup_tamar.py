async def main():
    conn = await asyncpg.connect(DATABASE_URL)

    # יצירת רשות תמר
    tamar_id = str(uuid.uuid4())
    existing = await conn.fetchrow("SELECT id FROM municipalities WHERE code = 'TAM'")
    if existing:
        tamar_id = str(existing["id"])
        print(f"Tamar already exists: {tamar_id}")
    else:
        await conn.execute(
            "INSERT INTO municipalities (id, name, code) VALUES ($1, $2, $3)",
            tamar_id, "מ.א תמר", "TAM"
        )
        print(f"Created Tamar: {tamar_id}")

    # template bezeq
    tmpl = await conn.fetchrow("SELECT id FROM templates WHERE name = 'bezeq'")
    tmpl_id = str(tmpl["id"])

    # טעינת אינדקסים
    created = 0
    for phone, account, desc in TAMAR_INDEXES:
        await conn.execute("""
            INSERT INTO indexes (id, municipality_id, template_id, key_value, account_code, description, connection_name)
            VALUES ($1, $2, $3, $4, $5, $6, $7)
            ON CONFLICT (municipality_id, template_id, key_value)
            DO UPDATE SET account_code = EXCLUDED.account_code,
                          connection_name = EXCLUDED.description,
                          updated_at = NOW()
        """, str(uuid.uuid4()), tamar_id, tmpl_id, phone, account, desc, desc)
        created += 1

    print(f"Loaded {created} indexes for Tamar.")
    print(f"\nTamar municipality ID: {tamar_id}")
    print("Add this to your notes!")
    await conn.close()

asyncio.run(main())
