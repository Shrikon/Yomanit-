async def main():
    conn = await asyncpg.connect(DATABASE_URL)

    # עדכן שם רשות
    await conn.execute(
        "UPDATE municipalities SET name = 'מ.א חבל מודיעין' WHERE id = $1",
        MITAR_ID
    )
    print("Updated municipality name to מ.א חבל מודיעין")

    # ודא שקיים template חשמל
    tmpl = await conn.fetchrow("SELECT id FROM templates WHERE name = 'electricity'")
    if not tmpl:
        tmpl_id = str(uuid.uuid4())
        await conn.execute(
            "INSERT INTO templates (id, name, display_name, key_field) VALUES ($1, 'electricity', 'חשמל', 'meter')",
            tmpl_id
        )
        print(f"Created electricity template: {tmpl_id}")
    else:
        tmpl_id = str(tmpl["id"])
        print(f"Using existing template: {tmpl_id}")

    # הוסף חשבון ספק חשמל להגדרות
    await conn.execute("""
        INSERT INTO municipality_settings (id, municipality_id, template_name, key, value)
        VALUES ($1, $2, 'electricity', 'vendor_account', '7000000000')
        ON CONFLICT (municipality_id, template_name, key) DO NOTHING
    """, str(uuid.uuid4()), MITAR_ID)

    # נקה אינדקסים קיימים
    deleted = await conn.execute(
        "DELETE FROM indexes WHERE municipality_id = $1 AND template_id = $2",
        MITAR_ID, tmpl_id
    )
    print(f"Cleared: {deleted}")

    # טען אינדקסים חדשים
    created = 0
    for contract, account, name, percent in INDEXES:
        desc = str(percent)
        await conn.execute("""
            INSERT INTO indexes (id, municipality_id, template_id, key_value,
                                 account_code, description, connection_name)
            VALUES ($1, $2, $3, $4, $5, $6, $7)
            ON CONFLICT (municipality_id, template_id, key_value, account_code)
            DO UPDATE SET connection_name = EXCLUDED.connection_name,
                          updated_at = NOW()
        """, str(uuid.uuid4()), MITAR_ID, tmpl_id,
             contract, account, desc, name)
        created += 1

    print(f"Loaded {created} electricity indexes for Mitar.")
    await conn.close()


asyncio.run(main())
